package com.company;

public class CalculatorObject {

    // methods for int parameters

    public int add(int integerA, int integerB) {
        return integerA + integerB;
    }

    public int subtract(int integerA, int integerB) {
        return integerA - integerB;
    }

    public int multiply(int integerA, int integerB) {
        return integerA * integerB;
    }

    public int divide(int integerA, int integerB) {
        return integerA / integerB;
    }

// methods for double parameters

    public double addDouble(double doubleA, double doubleB) {
        return doubleA + doubleB;
    }

    public double subtractDouble(double doubleA, double doubleB) {
        return doubleA - doubleB;
    }

    public double multiplyDouble(double doubleA, double doubleB) {
        return doubleA * doubleB;
    }

    public double divideDouble(double doubleA, double doubleB) {
        return doubleA / doubleB;
    }
}